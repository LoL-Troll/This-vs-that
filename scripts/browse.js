function showPhoneBrands() {
  var checkbox = (document.getElementById('phone'));

  const box = document.getElementById('phone-brands-filter');

  if (checkbox.checked) {
    box.style.display = 'block';
  } else {
    box.style.display = 'none';
  }
}

